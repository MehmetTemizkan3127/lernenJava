package benimcalismalarim.konular;

public class J5Ifconditions {
    /*
    05- If Conditions (Koşul İfadeleri)
Tanım:
if yapısı, belirli bir koşul doğruysa bir kod bloğunu çalıştırmaya yarar. if, else if, ve else gibi yapıların birleşimiyle farklı durumlar yönetilebilir.

Kullanım:
java
Kodu kopyala
if (condition) {
    // Koşul doğruysa bu blok çalışır
} else if (anotherCondition) {
    // İlk koşul yanlışsa, bu blok çalışır
} else {
    // Hiçbiri doğru değilse bu blok çalışır
}
Örnek:
java
Kodu kopyala
int age = 20;
if (age >= 18) {
    System.out.println("Ergin");
} else {
    System.out.println("Çocuk");
}
Koşul İfadeleri	Açıklama	Örnek Kullanım
if	Koşul doğruysa çalışacak blok.	if (x > 5)
else if	Birden fazla koşul kontrolü için kullanılır.	else if (x == 5)
else	Hiçbir koşul sağlanmazsa çalışacak blok.	else
Önemli Not:
Birden fazla else if kullanılabilir, ancak sadece bir else bloğu bulunabilir.
Koşul ifadelerinde boolean türü kullanılır.
Konu Özeti:
if yapısı, koşullu işlemler için temel yapıdır.
Koşul kontrolünde mantık hataları yapılmamalıdır.
     */
}
