package benimcalismalarim.konular;

public class J10Methodcreation {
    /*
    10- Method Creation (Metod Oluşturma)
Tanım:
Metodlar, belirli bir işlevi yerine getiren, bir kez tanımlanıp defalarca çağrılabilen kod bloklarıdır.

Metod Tanımlama:
Bir metodun temel yapısı:

java
Kodu kopyala
returnType methodName(parameters) {
    // Kod bloğu
}
returnType: Metodun geri döndüreceği tür.
methodName: Metodun adı.
parameters: (Opsiyonel) Metoda gönderilecek argümanlar.
Örnek:
java
Kodu kopyala
public int add(int a, int b) {
    return a + b;
}
Önemli Metod Tipleri:
Void Metodlar: Değer döndürmeyen metodlar.

java
Kodu kopyala
public void printMessage() {
    System.out.println("Merhaba");
}
Değer Döndüren Metodlar: Geriye değer döndüren metodlar.

java
Kodu kopyala
public int multiply(int a, int b) {
    return a * b;
}
Method Overloading (Aşırı Yükleme):
Aynı isimde farklı parametre sayısı veya türüne sahip metodlar oluşturulabilir.

java
Kodu kopyala
public int add(int a, int b) {
    return a + b;
}

public double add(double a, double b) {
    return a + b;
}
Önemli Not:
Metodlar, kodun tekrarını önler ve modülerlik sağlar.
void metodları değer döndürmezken, diğer metodlar dönüş türüne göre veri döndürmelidir.
Konu Özeti:
Metodlar, belirli bir işlemi tekrar edilebilir şekilde tanımlar.
Parametreler ve dönüş türlerine dikkat edilmelidir.

     */
}
