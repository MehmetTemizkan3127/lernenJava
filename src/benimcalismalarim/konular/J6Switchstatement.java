package benimcalismalarim.konular;

public class J6Switchstatement {
    /*
06- Switch Statement (Switch Durumu)
Tanım:
switch ifadesi, belirli bir değişkenin değerine göre farklı durumları kontrol etmeye yarar. switch genellikle eşitlik (==) kontrolü yapar.

Kullanım:
java
Kodu kopyala
switch (variable) {
    case value1:
        // value1 için yapılacak işlem
        break;
    case value2:
        // value2 için yapılacak işlem
        break;
    default:
        // Hiçbir case ile eşleşmeyen durum
}
Örnek:
java
Kodu kopyala
int day = 2;
switch (day) {
    case 1:
        System.out.println("Pazartesi");
        break;
    case 2:
        System.out.println("Salı");
        break;
    default:
        System.out.println("Geçersiz gün");
}
Durum	Açıklama	Örnek Kullanım
case	Değişkenin değerine göre kontrol sağlar.	case 1:
break	switch bloğundan çıkılmasını sağlar.	break;
default	Hiçbir case eşleşmediğinde çalışacak kod bloğu.	default:
Önemli Not:
break komutu ile kontrol yapılmazsa, bir sonraki case bloğu da çalışır (fall-through).
switch yalnızca eşitlik (==) karşılaştırması yapar, > < gibi karşılaştırmalar kullanılamaz.
Konu Özeti:
switch, belirli bir değeri farklı seçeneklerle karşılaştırarak işlem yapar.
break komutunun eksikliği yanlış çalışmalara yol açabilir.

     */
}
