package benimcalismalarim.konular;

public class J7StringManipulations {
    /*
    07- String Manipulations (String Manipülasyonları)
Tanım:
Java'da String, karakter dizilerini temsil eder ve immutable (değiştirilemez) bir sınıftır. String ile çeşitli manipülasyonlar yapılabilir.

Temel String Metodları:
length(): String'in uzunluğunu döndürür.
charAt(int index): Belirtilen index'teki karakteri döndürür.
substring(int start, int end): String'den belirli bir kısmı çıkarır.
toUpperCase(): String'in tamamını büyük harfe dönüştürür.
toLowerCase(): String'in tamamını küçük harfe dönüştürür.
contains(): String içinde belirli bir kelimenin olup olmadığını kontrol eder.
java
Kodu kopyala
String text = "Merhaba Java";
System.out.println(text.length()); // 12
System.out.println(text.charAt(0)); // 'M'
System.out.println(text.substring(0, 7)); // "Merhaba"
Önemli Not:
String sınıfı immutable olduğu için yapılan her değişiklik yeni bir String oluşturur.
equals() metodu ile iki String karşılaştırılmalıdır, == ile değil.
Konu Özeti:
String manipülasyonları ile veriler üzerinde işlem yapılabilir.
String'in immutable olmasına dikkat edilmelidir.
     */
}
