package benimcalismalarim.konular;

public class J8Loops {
/*
08- Loops (Döngüler)
Tanım:
Döngüler, belirli bir koşul sağlandığı sürece aynı kod bloğunu tekrarlamak için kullanılır.

Türleri:
for: Belirli sayıda yineleme için kullanılır.
java
Kodu kopyala
for (int i = 0; i < 10; i++) {
    System.out.println(i);
}
while: Koşul doğru olduğu sürece döner.
java
Kodu kopyala
int i = 0;
while (i < 10) {
    System.out.println(i);
    i++;
}
do-while: En az bir kez çalışır, ardından koşul kontrol edilir.
java
Kodu kopyala
int i = 0;
do {
    System.out.println(i);
    i++;
} while (i < 10);
Önemli Not:
for döngüsü genellikle bilinen bir sayıdaki tekrarlar için, while ve do-while döngüleri ise koşullar dinamikse daha uygundur.
Konu Özeti:
Döngüler belirli bir kod bloğunu tekrar etmek için kullanılır.
Koşul ifadelerine dikkat edilmelidir, yanlış koşullar sonsuz döngülere yol açabilir.
 */
}

