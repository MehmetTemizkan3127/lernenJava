package benimcalismalarim.önceki.konular;

public class J11Arrays {
    /*
    11- Arrays (Diziler)
Tanım:
Diziler, aynı türdeki birden fazla veriyi tek bir değişkende tutan veri yapılandırmalarıdır.

Dizi Tanımlama:
java
Kodu kopyala
int[] arr = new int[5]; // 5 elemanlı bir dizi oluşturulur.
Diziye Eleman Atama:
java
Kodu kopyala
arr[0] = 10;
arr[1] = 20;
arr[2] = 30;
Diziye Başlangıç Değeri Atama:
java
Kodu kopyala
int[] arr = {10, 20, 30, 40, 50};
Dizi Boyutunu Öğrenme:
java
Kodu kopyala
int size = arr.length;
Örnek Dizi Kullanımı:
java
Kodu kopyala
int[] numbers = {1, 2, 3, 4, 5};
for (int i = 0; i < numbers.length; i++) {
    System.out.println(numbers[i]);
}
Dizi Tipi	Örnek Tanımlama	Açıklama
1D Dizi	int[] arr = {1, 2, 3};	Tek boyutlu dizi.
2D Dizi	int[][] matrix = {{1, 2}, {3, 4}};	İki boyutlu dizi.
Çok Boyutlu Dizi	int[][][] cube = {{{1}}, {{2}}};	Çok boyutlu dizilerde veri depolama.
Önemli Not:
Diziler sabit boyutludur; dinamik büyüklükler için ArrayList kullanın.
Dizilerde index sıfırdan başlar.
Konu Özeti:
Diziler, benzer türdeki birden fazla veriyi tek bir yapıda depolar.
Dizi boyutları sabittir ve index sıfırdan başlar.

     */
}
