package benimcalismalarim.önceki.konular;

public class J13ForeachLoop {
    /*
    13- Foreach Loop
Tanım:
foreach döngüsü, özellikle koleksiyonlar ve diziler gibi veri yapılarını iterasyonla gezmek için kullanılır.

Kullanım:
java
Kodu kopyala
int[] arr = {1, 2, 3, 4};
for (int num : arr) {
    System.out.println(num);
}
Önemli Not:
foreach döngüsü, yalnızca koleksiyonun her elemanını okur. Elemanlar üzerinde değişiklik yapılamaz.
Konu Özeti:
foreach döngüsü, koleksiyonlar ve diziler üzerinde gezinmeyi kolaylaştırır.
Veri üzerinde değişiklik yapamazsınız.

     */
}
