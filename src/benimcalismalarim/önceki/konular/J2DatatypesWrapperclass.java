package benimcalismalarim.önceki.konular;

public class J2DatatypesWrapperclass {/*
02- Datatypes ve Wrapper Class
Datatypes (Veri Tipleri):
Verilerin türlerini belirtir. İki tür veri tipi vardır:

Primitive: Doğrudan bellekte saklanır.
Non-primitive: Referanslar aracılığıyla kullanılır.
Primitive Türler	Default Değer	Örnek
byte	0	byte b = 10;
short	0	short s = 20;
int	0	int i = 100;
long	0L	long l = 100000L;
float	0.0f	float f = 10.5f;
double	0.0	double d = 20.99;
char	'\u0000'	char c = 'A';
boolean	false	boolean b = true;
Wrapper Class:
Primitive veri tiplerini obje olarak temsil eden sınıflardır. Örneğin:

int için Integer
double için Double
java
Kodu kopyala
int primitive = 5;
Integer wrapper = Integer.valueOf(primitive); // Primitive türünü Wrapper'a çevirme
Önemli Not:
Wrapper sınıfları, autoboxing ve unboxing işlemleriyle primitive türler arasında geçiş yapar.
Konu Özeti:
Primitive tipler, bellek tasarrufu sağlar.
Wrapper sınıflar, daha fazla metot ve işlevsellik sunar.


*/
}
