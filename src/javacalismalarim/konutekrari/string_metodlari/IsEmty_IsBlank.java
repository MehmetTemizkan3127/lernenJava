package javacalismalarim.konutekrari.string_metodlari;

public class IsEmty_IsBlank {
    public static void main(String[] args) {

       //IsEmty
        String str1 = "";
        System.out.println("str1 = " + str1.isEmpty());

        String str2 = " ";
        System.out.println("str2 = " + str2.isEmpty());


        //IsBlank
        String kelime1 = "";
        System.out.println("str1 = " + kelime1.isBlank());

        String kelime2 = " ";
        System.out.println("str2 = " + kelime2.isBlank());





    }
}
